# Create an empty set
my_set = set()

# Add members to the set
my_set.add(10)
my_set.add(20)
my_set.add(30)

# Print the set after adding items
print("Set after adding items:", my_set)

# Remove one item from the set
my_set.remove(20)

# Print the set after removing an item
print("Set after removing an item:", my_set)
